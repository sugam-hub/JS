function mathFunction(a, b) {
    document.write("The sum is: "+ parseInt(a + b) + "<br/>");  
    document.write("The difference is: "+ parseInt(a-b) + "<br/>");  
    parseInt(document.write("The product is: "+ a * b + "<br/>"));  
    parseInt(document.write("The quotient is: "+ a / b + "<br/>"));  
    parseInt(document.write("The remainder is: "+ a % b + "<br/>"));  
}
mathFunction(10, 5);